# %100d 共100位  %0.2f 前面 0位，後面2位。
print("Happy : %100d, Python : %0.2f" % (1, 05.333))

print("Total students : %100d, Boys : %1d" % (240, 120))

# using format() 方法(填入它
print('I love {} for "{}!"'.format('Python', 'CTFer'))

# 使用位置表達 ==>有何不同
print('{0} and {1}'.format('Python', 'CTFer'))
 
print('{1} and {0}'.format('Python', 'CTFer'))
